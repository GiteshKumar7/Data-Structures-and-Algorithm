// certain operation of the BIT Manipulation
// Bit manipulation

public class B24 {
    public static void main(String args[]) {

  // binary bitwise  And (&) operators
        System.out.println(5 & 6);

    // binary bitwise  OR(|) operators 
        System.out.println(5|6);

    // binary bitwise NOt (~)or binary one's complement operators
       System.out.println(~0);
       System.out.println(~5);

       // binary left shift(<<)
       System.out.println(5<<2);

       // binary right shift(>>)
       System.out.println(5>>2);


    }

}
